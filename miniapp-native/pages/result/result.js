const api = require('../../utils/api.js')
const marked = require('../../utils/marked.js')
const API = 'https://gjz.shadouyou.cloud/api'

function filterText(t){return t?t.replace(/DeepSeek/gi,'').replace(/深度求索/gi,'').replace(/由.*?生成/g,'').replace(/Generated by.*$/gim,'').replace(/\n*>?\s*以上内容由.*?生成.*?\n*$/gm,'').replace(/\n*>?\s*This content was generated by.*?\n*$/gim,''):''}

const lineTypeCN={'old_yin':'老阴','young_yang':'少阳','young_yin':'少阴','old_yang':'老阳'}

function tossCoin(){return Math.random()<0.5?3:2}
function castSixLines(){const r=[];for(let i=0;i<6;i++)r.push(tossCoin()+tossCoin()+tossCoin());return r}

Page({
  data:{
    mode:'new',phase:'done',questionRef:'',
    tossResults:[],yaoNames:['初爻','二爻','三爻','四爻','五爻','上爻'],
    hexagram:{primary:'',changing:''},
    aiText:'',rawAiText:'',error:'',statusMsg:'',showMaster:false,
    dots:'...',dotsTimer:null,
    loadingFromDB:false,noHistory:false,remainingQuota:-1,
    historyId:null,currentHistoryId:null,
    historyLang:'zh',translationText:'',isTranslating:false,translateError:'',
    showResultContent:false,showInterpret:false,needsTranslateBanner:false,needsTranslateLabel:'',renderedHTML:'',
    animThrow:1,animCoins:[null,null,null],isAnimating:true,
    statusMap:{coins:'起卦中...',result:'卦象已现',interpretation:'AI 解读中...',done:'解读完成',error:'出错了'}
  },
  syncDerived(){
    const d=this.data
    const sr=d.mode==='db'||['result','interpretation','done'].includes(d.phase)
    const si=['interpretation','done'].includes(d.phase)
    const nt=d.mode==='db'&&d.historyLang==='en'&&!d.translationText
    let h=marked.parse(d.translationText||d.aiText||'')
    if(d.phase==='interpretation'&&!d.translationText)h+='<span style="animation:blink 1s infinite">▊</span>'
    this.setData({showResultContent:sr,showInterpret:si,needsTranslateBanner:nt,needsTranslateLabel:'⚠ This reading was generated in English',renderedHTML:h})
  },
  onLoad(o){
    const q=getApp().globalData?.question||'',hid=o?.historyId||''
    if(hid){this.setData({mode:'db',phase:'done',loadingFromDB:true,historyId:hid,questionRef:q});this.syncDerived();this.loadHistory(hid)}
    else if(!q){this.setData({mode:'db',loadingFromDB:true});this.syncDerived();this.loadLatestFromDB()}
    else{this.setData({mode:'new',phase:'coins',questionRef:q});this.syncDerived();this.startDivination()}
  },
  onUnload(){if(this.data.dotsTimer)clearInterval(this.data.dotsTimer)},
  onShareAppMessage(){return{title:'我在观己斋占了一卦「'+this.data.hexagram.primary+'」→「'+this.data.hexagram.changing+'」',path:'/pages/index/index'}},
  startDivination(){
    const lines=castSixLines(),names=['初爻','二爻','三爻','四爻','五爻','上爻'],nums={2:'反',3:'正'}
    const coinPats={6:[2,2,2],7:[2,2,3],8:[2,3,3],9:[3,3,3]}
    this.data.currToss=0
    const playNext=()=>{
      if(this.data.currToss>=6){this.divineAPI(lines);return}
      const i=this.data.currToss,v=lines[i],r=['old_yin','young_yang','young_yin','old_yang'][(v-6)%4]
      const cv=coinPats[v]||[2,2,2]
      const tr=[...this.data.tossResults,{throw:i+1,label:names[i],coin_values:cv.map(c=>nums[c]||'反'),sum:v,result:lineTypeCN[r],is_changing:r==='old_yin'||r==='old_yang',yaoValue:r==='young_yang'||r==='old_yang'}]
      this.setData({tossResults:tr,animThrow:i+1,animCoins:cv.map(c=>nums[c]||'反'),isAnimating:true});this.syncDerived()
      this.data.currToss++;setTimeout(playNext,200)
    }
    playNext()
  },
  divineAPI(lines){
    this.setData({phase:'result',dotsTimer:setInterval(()=>{const a=['.','..','...','....'],d=this.data.dots,i=a.indexOf(d);this.setData({dots:a[(i+1)%4]})},500)})
    wx.request({url:API+'/divine',method:'POST',header:{'Content-Type':'application/json','Authorization':'Bearer '+wx.getStorageSync('token')},data:{question:this.data.questionRef},
    success:r=>{
      if(r.statusCode===200&&r.data.primary){
        const d=r.data
        this.setData({phase:'done',isAnimating:false,hexagram:{primary:d.primary.name,changing:d.changing.name},rawAiText:d.interpretation||'',aiText:filterText(d.interpretation||''),remainingQuota:d.remaining_quota??-1});this.syncDerived()
        if(this.data.dotsTimer)clearInterval(this.data.dotsTimer)
      }else{this.setData({error:r.data?.error||'解卦失败',phase:'error'});this.syncDerived()}
    },
    fail:()=>{this.setData({error:'网络连接失败',phase:'error'});this.syncDerived()}})
  },
  loadHistory(id){
    api.history(100,0).then(d=>{const a=d.items||[],h=a.find(i=>i.id==id)
      if(h){this.setData({loadingFromDB:false,hexagram:{primary:h.primary_gua,changing:h.changing_gua},aiText:h.interpretation||'',rawAiText:h.interpretation||'',historyLang:h.lang||'zh'});this.syncDerived()}
      else{this.setData({error:'记录不存在',phase:'error',loadingFromDB:false});this.syncDerived()}
    }).catch(()=>{this.setData({error:'加载失败',phase:'error',loadingFromDB:false});this.syncDerived()})
  },
  loadLatestFromDB(){
    api.history(1,0).then(d=>{const a=d.items||[]
      if(a.length===0){this.setData({noHistory:true,loadingFromDB:false});this.syncDerived();return}
      const h=a[0];this.setData({loadingFromDB:false,hexagram:{primary:h.primary_gua,changing:h.changing_gua},aiText:h.interpretation||'',rawAiText:h.interpretation||'',questionRef:h.question||'',currentHistoryId:h.id,historyLang:h.lang||'zh'});this.syncDerived()
    }).catch(()=>{this.setData({error:'加载失败',phase:'error',loadingFromDB:false});this.syncDerived()})
  },
  doTranslate(){
    const id=this.data.historyId||this.data.currentHistoryId
    if(!id||this.data.isTranslating)return
    this.setData({isTranslating:true,translateError:''})
    wx.request({url:API+'/history/'+id+'/translate?target=en',method:'POST',header:{Authorization:'Bearer '+wx.getStorageSync('token')},timeout:60000,
    success:r=>{if(r.statusCode===200&&r.data.content){this.setData({translationText:filterText(r.data.content),isTranslating:false});this.syncDerived()}else{this.setData({translateError:r.data?.error||'翻译失败',isTranslating:false})}},
    fail:()=>{this.setData({translateError:'网络错误',isTranslating:false})}})
  },
  toggleMaster(){this.setData({showMaster:!this.data.showMaster})},
  goHome(){wx.reLaunch({url:'/pages/index/index'})},
  goHistory(){wx.navigateTo({url:'/pages/history/history'})},
})
