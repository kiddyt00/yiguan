const api = require('../../utils/api.js')
const marked = require('../../utils/marked.js')
const API = 'https://gjz.shadouyou.cloud/api'

function filterText(text) {
  if (!text) return ''
  return text
    .replace(/DeepSeek/gi, '')
    .replace(/深度求索/gi, '')
    .replace(/由.*?生成/g, '')
    .replace(/Generated by.*$/gim, '')
    .replace(/\n*>?\s*以上内容由.*?生成.*?\n*$/gm, '')
    .replace(/\n*>?\s*This content was generated by.*?\n*$/gim, '')
}

Page({
  // 自动在每次 setData 后同步派生字段
  _origSetData: Page.prototype.setData,
  setData(data, callback) {
    this._origSetData(data, () => {
      if (this.syncDerived) this.syncDerived()
      if (callback) callback()
    })
  },
  data: {
    mode: 'new', phase: 'done', questionRef: '',
    tossResults: [],
    yaoNames: ['初爻','二爻','三爻','四爻','五爻','上爻'],
    hexagram: { primary: '', changing: '' },
    aiText: '', rawAiText: '', error: '', statusMsg: '', showMaster: false,
    dots: '...', dotsTimer: null, sseBuffer: '',
    loadingFromDB: false, noHistory: false,
    remainingQuota: -1,
    historyId: null, currentHistoryId: null,
    historyLang: 'zh', translationText: '', isTranslating: false, translateError: '',
    statusMap: { coins:'起卦中...', result:'卦象已现', interpretation:'AI 解读中...', done:'解读完成', error:'出错了' }
  },
  get nd() { return this.data },
  // 更新派生字段（模拟 computed）
  syncDerived() {
    const d = this.data
    const showResult = d.mode === 'db' || ['result','interpretation','done'].includes(d.phase)
    const showInterp = ['interpretation','done'].includes(d.phase)
    const needTrans = d.mode === 'db' && d.historyLang && d.historyLang === 'en' && !d.translationText
    let html = marked.parse(d.translationText || d.aiText || '')
    if (d.phase === 'interpretation' && !d.translationText) html += '<span style="animation:blink 1s infinite">▊</span>'
    this.setData({
      showResultContent: showResult,
      showInterpret: showInterp,
      needsTranslateBanner: needTrans,
      needsTranslateLabel: '⚠ This reading was generated in English',
      renderedHTML: html
    })
  },
  onLoad(options) {
    const q = getApp().globalData?.question || ''
    const histId = options?.historyId || ''
    const histLang = options?.lang || ''
    if (histId) {
      this.setData({ mode:'db', phase:'done', loadingFromDB:true, historyId:histId, historyLang:histLang, questionRef:q })
      this.syncDerived()
      this.loadHistory(histId)
    } else if (!q) {
      this.setData({ mode:'db', loadingFromDB:true })
      this.syncDerived()
      this.loadLatestFromDB()
    } else {
      this.setData({ mode:'new', phase:'coins', questionRef:q })
      this.syncDerived()
      this.startDots(); this.startStream()
    }
  },
  onUnload() { if (this.data.dotsTimer) clearInterval(this.data.dotsTimer) },
  onShareAppMessage() {
    return { title:'我在观己斋占了一卦「'+this.data.hexagram.primary+'」→「'+this.data.hexagram.changing+'」', path:'/pages/index/index' }
  },
  startDots() {
    const dots=['.','..','...','....']; let i=0
    const timer=setInterval(()=>{this.setData({dots:dots[i%4]});i++},500)
    this.data.dotsTimer=timer
  },
  loadHistory(id) {
    wx.request({
      url:API+'/admin/hexagrams/'+id, header:{'Authorization':'Bearer '+wx.getStorageSync('token')},
      success:(res)=>{
        if(res.statusCode===200){
          const d=res.data
          this.setData({ phase:'done', loadingFromDB:false, hexagram:{primary:d.primary_gua,changing:d.changing_gua}, aiText:d.interpretation||'',rawAiText:d.interpretation||'', historyLang:d.lang||'zh' }); this.syncDerived()
        }else{ this.setData({error:'加载失败',phase:'error',loadingFromDB:false}); this.syncDerived() }
      },
      fail:()=>this.setData({error:'网络错误',phase:'error',loadingFromDB:false})
    })
  },
  loadLatestFromDB() {
    api.history(1,0).then(data=>{
      const items=data.items||[]
      if(items.length===0){ this.setData({noHistory:true,loadingFromDB:false}); return }
      const h=items[0]
      this.setData({ phase:'done', loadingFromDB:false, hexagram:{primary:h.primary_gua,changing:h.changing_gua}, aiText:h.interpretation||'',rawAiText:h.interpretation||'', questionRef:h.question||'', currentHistoryId:h.id, historyLang:h.lang||'zh' })
    }).catch(()=>this.setData({error:'加载失败',phase:'error',loadingFromDB:false}))
  },
  startStream() {
    const q=this.data.questionRef
    if(!q){ this.setData({error:'问题为空',phase:'error'}); return }
    const token=wx.getStorageSync('token')
    const rt=wx.request({ url:API+'/divine/stream', method:'POST', header:{'Content-Type':'application/json','Authorization':'Bearer '+token}, data:{question:q}, enableChunked:true, responseType:'text', success:()=>{}, fail:(err)=>this.setData({error:'网络连接失败: '+(err.errMsg||''),phase:'error'}) })
    this.data.sseBuffer=''; this.data.rawAiText=''
    rt.onChunkReceived((res)=>{
      let chunk=''
      if(typeof res.data==='string') chunk=res.data
      else if(res.data instanceof ArrayBuffer) chunk=new TextDecoder('utf-8').decode(new Uint8Array(res.data))
      else if(res.data instanceof Uint8Array) chunk=new TextDecoder('utf-8').decode(res.data)
      this.data.sseBuffer+=chunk; this.drainBuffer()
    })
  },
  drainBuffer() {
    let idx
    while((idx=this.data.sseBuffer.indexOf('\n\n'))!==-1){
      const block=this.data.sseBuffer.slice(0,idx)
      this.data.sseBuffer=this.data.sseBuffer.slice(idx+2)
      this.parseBlock(block)
    }
  },
  parseBlock(block) {
    const lines=block.split('\n'); let event='',dataStr=''
    for(const l of lines){ if(l.startsWith('event:')) event=l.slice(6).trim(); else if(l.startsWith('data:')) dataStr=l.slice(5).trim() }
    if(!dataStr) return
    try{
      const d=JSON.parse(dataStr)
      if(event==='phase'){
        if(d.phase==='coins'){
          const t=d.data; const r=[...(this.data.tossResults||[])]
          r.push({throw:t.throw,label:t.label,result:t.result,sum:t.sum,coin_values:t.coin_values||[],is_changing:t.result==='老阴'||t.result==='老阳',yaoValue:t.result==='少阳'||t.result==='老阳'})
          this.setData({phase:'coins',tossResults:r})
        }else if(d.phase==='hexagram'){
          this.setData({phase:'result',hexagram:{primary:d.data.primary_gua,changing:d.data.changing_gua}})
        }
      }else if(event==='ai'){
        if(this.data.phase==='result') this.setData({phase:'interpretation'})
        const nt=(this.data.rawAiText||'')+(d.chunk||'')
        this.setData({rawAiText:nt,aiText:filterText(nt)})
      }else if(event==='status'){ this.setData({statusMsg:d.msg||''}) }
      else if(event==='done'){
        api.profile().then(p=>this.setData({remainingQuota:p.remaining_quota??-1})).catch(()=>{})
        this.setData({phase:'done',historyLang:'zh'})
      }else if(event==='error'){ this.setData({error:d.error,phase:'done'}) }
    }catch(e){}
  },
  doTranslate() {
    const id=this.data.historyId||this.data.currentHistoryId
    if(!id||this.data.isTranslating) return
    this.setData({isTranslating:true,translateError:''})
    wx.request({
      url:API+'/history/'+id+'/translate?target=en', method:'POST',
      header:{'Authorization':'Bearer '+wx.getStorageSync('token')}, timeout:60000,
      success:(res)=>{
        if(res.statusCode===200&&res.data.content) this.setData({translationText:filterText(res.data.content),isTranslating:false})
        else this.setData({translateError:res.data?.error||'翻译失败',isTranslating:false})
      },
      fail:()=>this.setData({translateError:'网络错误',isTranslating:false})
    })
  },
  toggleMaster(){this.setData({showMaster:!this.data.showMaster})},
  goHome(){wx.reLaunch({url:'/pages/index/index'})},
  goHistory(){wx.navigateTo({url:'/pages/history/history'})},
})
